import java.util.Scanner;
public class CharExtraction {
public static void main(String[] args) {
String message = "Java";
 
char first = message.charAt(0);  
char second = message.charAt(1); 
char third = message.charAt(2);  
char fourth = message.charAt(3); 
        
System.out.println("First character: " + first);
System.out.println("Second character: " + second);
System.out.println("Third character: " + third);
System.out.println("Fourth character: " + fourth);
}
}

